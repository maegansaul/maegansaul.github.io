// Function 2 - Keys to String ///////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function keysToString(object) {
//create something to hold the keys
var keyArray = [];
//take all keys out of the object
for (var key in object){
//place the keys into the empty array
    keyArray.push(key); 
//return this result with a space in between
}return keyArray.join(' ');
} 

